# json.dump()
import json

numbers = [1, 2, 3, 4, 5, 6, 7,]
filename = 'numbers.json'

with open(filename, 'w') as f_obj :
    json.dump(numbers, f_obj)
#-----------------------------------------------------------
# json.load()
with open(filename) as file_obj :
    nums = json.load(file_obj)

print(nums)

