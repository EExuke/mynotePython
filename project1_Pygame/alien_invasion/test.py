import pygame
#-----------------------------------------------------------
#                  MAIN PROCESS
#-----------------------------------------------------------
pygame.init()
while True:
    for event in pygame.event.get():
        # 退出事件
        if event.type == pygame.KEYDOWN:
            print(event.key)


